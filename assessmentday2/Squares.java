package assessmentday2;

public class Squares {
	    public static void main(String[] args) {
	        System.out.println("Squares of numbers from 1 to 10:");
	        for (int i = 1; i <= 10; i++) {
	            System.out.println("Square of " + i + " is: " + (i * i));
	        }
	    }
	}


